function getImages(baseName, count, format = ".jpg") {
    let path = "./media/intro/"; // 圖片資料夾路徑
    let arr = [];

    // 1. 加入主圖 (例如 id01.jpg)
    arr.push(`${path}${baseName}${format}`);

    // 2. 自動迴圈加入子圖 (例如 id01-1.jpg 到 id01-14.jpg)
    for (let i = 1; i <= count; i++) {
        arr.push(`${path}${baseName}-${i}${format}`);
    }

    return arr;
}
